/**
 * @desc 获取多选按钮选中状态值
 * @param {string} elementId
 * @returns {int} 0未选中或1选中
 */
function hasClass(elementId) {
    if ($("#" + elementId).hasClass("live")) {
        return 1;
    } else {
        return 0;
    }
}


/**
 * @desc 获取checkBox选中状态值
 * @param {string} checkBoxId
 * @returns {int}0未选中或1选中
 */
function getCheckBoxVal(checkBoxId) {
    if ($('#' + checkBoxId).is(':checked')) {
        return 1;
    }
    return 0;
}